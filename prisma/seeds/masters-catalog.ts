const MASTERS_PER_CATEGORY = 10;
const SERVICES_PER_MASTER_MIN = 6;
const SERVICES_PER_MASTER_MAX = 10;

const pickRotated = <T>(
  items: readonly T[],
  start: number,
  count: number,
): T[] => {
  if (items.length === 0) {
    return [];
  }

  const result: T[] = [];
  for (let i = 0; i < count; i += 1) {
    result.push(items[(start + i) % items.length]);
  }
  return result;
};

const varyService = (
  template: ServiceTemplate,
  masterIndex: number,
  serviceIndex: number,
): MasterServiceSeed => {
  const priceFactor = 1 + ((masterIndex + serviceIndex) % 5) * 0.05;
  const durationDelta = ((masterIndex + serviceIndex) % 3) * 5;
  const extraTags = [
    `пакет-${(masterIndex % 5) + 1}`,
    `район-${(serviceIndex % 7) + 1}`,
    masterIndex % 2 === 0 ? 'выезд' : 'студия',
    serviceIndex % 2 === 0 ? 'онлайн-запись' : 'очно',
  ];

  const tags = normalizeTags([...template.tags, ...extraTags]);

  return {
    name:
      serviceIndex % 4 === 0 ? `${template.name} (расширенный)` : template.name,
    description: template.description,
    price: Math.round(template.price * priceFactor),
    durationMinutes: template.durationMinutes + durationDelta,
    category: template.category,
    tags,
  };
};

const buildMastersForCategory = (
  category: MasterServiceCategory,
): MasterProfileSeed[] => {
  const pool = CATEGORY_SERVICE_POOLS[category];
  const blueprint = CATEGORY_BLUEPRINTS[category];
  const masters: MasterProfileSeed[] = [];

  for (
    let masterIndex = 0;
    masterIndex < MASTERS_PER_CATEGORY;
    masterIndex += 1
  ) {
    const prefix = blueprint.prefixes[masterIndex % blueprint.prefixes.length];
    const person =
      MASTER_FIRST_NAMES[
        (masterIndex + ALL_CATEGORIES.indexOf(category)) %
          MASTER_FIRST_NAMES.length
      ];
    const suffix =
      MASTER_NAME_SUFFIXES[masterIndex % MASTER_NAME_SUFFIXES.length];

    const servicesCount =
      SERVICES_PER_MASTER_MIN +
      (masterIndex % (SERVICES_PER_MASTER_MAX - SERVICES_PER_MASTER_MIN + 1));

    const templates = pickRotated(pool, masterIndex * 2, servicesCount);
    const uniqueByName = new Map<string, MasterServiceSeed>();

    templates.forEach((template, serviceIndex) => {
      const next = varyService(template, masterIndex, serviceIndex);
      const key = next.name;
      if (!uniqueByName.has(key)) {
        uniqueByName.set(key, next);
        return;
      }
      uniqueByName.set(`${key} #${serviceIndex + 1}`, {
        ...next,
        name: `${key} #${serviceIndex + 1}`,
      });
    });

    const rating = Number((4.1 + (masterIndex % 9) * 0.1).toFixed(1));

    masters.push({
      displayName: `${prefix} ${person} ${suffix}`,
      description:
        blueprint.descriptions[masterIndex % blueprint.descriptions.length],
      rating: Math.min(rating, 5),
      services: [...uniqueByName.values()],
    });
  }

  return masters;
};

