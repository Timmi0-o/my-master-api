const normalizeTags = (tags: readonly string[]): string[] => {
  const normalized = [
    ...new Set(
      tags
        .map((tag) => tag.trim().toLowerCase())
        .filter((tag) => tag.length > 0 && tag.length <= 50),
    ),
  ];

  if (normalized.length < 5) {
    throw new Error(
      `masters-catalog: service needs at least 5 tags, got ${normalized.length}`,
    );
  }

  if (normalized.length > 30) {
    return normalized.slice(0, 30);
  }

  return normalized;
};

const service = (
  name: string,
  description: string,
  price: number,
  durationMinutes: number,
  tags: readonly string[],
): ServiceTemplate => ({
  name,
  description,
  price,
  durationMinutes,
  category: 'SERVICES',
  tags: normalizeTags(tags),
});

const withCategory = (
  category: MasterServiceCategory,
  items: ServiceTemplate[],
): ServiceTemplate[] =>
  items.map((item) => ({
    ...item,
    category,
    tags: normalizeTags(item.tags),
  }));

